package webinar_3_4;

public class Crossroad {
    private int streetNumber;
    private int avenueNumber;

    public Crossroad(int streetNumber, int avenueNumber) {
        this.streetNumber = streetNumber;
        this.avenueNumber = avenueNumber;
    }

    public int getStreetNumber() {
        return streetNumber;
    }

    public int getAvenueNumber() {
        return avenueNumber;
    }

}
